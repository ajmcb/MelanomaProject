package com.example.moletracker;

public class Location {
    
    private String location;
    private String area; 


    public String setLocation(String nlocation) {
        location = nlocation;
    }

    public String getLocation() {
        return location;
    }

    public String setArea(String narea) {
        area = narea;
    }

    public String getArea() {
        return area;
    }

}
